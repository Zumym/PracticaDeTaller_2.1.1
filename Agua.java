public class Agua {

	private boolean ExisteAgua;
	private double CantidadDeAgua;

	public boolean isExisteAgua() {
		return this.ExisteAgua;
	}

	public void setExisteAgua(boolean ExisteAgua) {
		this.ExisteAgua = ExisteAgua;
	}

	public double getCantidadDeAgua() {
		return this.CantidadDeAgua;
	}

	public void setCantidadDeAgua(double CantidadDeAgua) {
		this.CantidadDeAgua = CantidadDeAgua;
	}

}